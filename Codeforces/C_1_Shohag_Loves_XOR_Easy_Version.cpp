#include <iostream>
#include <bits/stdc++.h>
using namespace std;
using ll = long long;
void solve()
{
    int x;
    ll m;
    cin >> x >> m;

    int ans = 0;
    for (int y = 1; y <= min(2LL * x, m); y++)
    {
        if (x != y and ((x % (x ^ y)) == 0 or (y % (x ^ y) == 0)))
        {
            ++ans;
        }
    }
    cout << ans << '\n';
}
int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL); cout.tie(NULL);
    int t;  cin>>t;
    while(t--){
        solve();
    }
    return 0;
}
